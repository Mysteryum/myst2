<p class="MsoNormal">
	<b style="mso-bidi-font-weight:normal"><span style="font-size:14.0pt;line-height:115%">ОБРАЩЕНИЕ<span style="mso-spacerun:yes">&nbsp; </span>К МИРОВОЙ КИНОЛОГИЧЕСКОЙ ОБЩЕСТВЕННОСТИ <span style="mso-spacerun:yes">&nbsp;&nbsp;&nbsp;&nbsp;</span>УЧАСТНИКОВ<span style="mso-spacerun:yes">&nbsp; </span><span style="mso-spacerun:yes">&nbsp;</span>3-ей<span style="mso-spacerun:yes">&nbsp; </span>КОНФЕРЕНЦИИ<span style="mso-spacerun:yes">&nbsp; </span>IKU .</span></b></p>
<p class="MsoNormal">
	<b style="mso-bidi-font-weight:normal"><span style="font-size:14.0pt;line-height:115%">&nbsp;</span></b></p>
<p class="MsoNormal">
	<span style="mso-spacerun:yes">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span>Прогресс<span style="mso-spacerun:yes">&nbsp; </span>человеческого общества всегда был связан с развитием различных сфер науки и жизнедеятельности , в ряду которых важное место всегда занимала кинология . Современная кинология , в первую очередь , социально-<span style="mso-spacerun:yes">&nbsp; </span>значима , социально- ответственна и является одним из факторов , формирующих у современного человека взгляды на отношение к живой природе . Именно <span style="mso-spacerun:yes">&nbsp;</span>поэтому <span style="mso-spacerun:yes">&nbsp;</span>основная <span style="mso-spacerun:yes">&nbsp;</span>мотивация <span style="mso-spacerun:yes">&nbsp;</span>кинологии - это любовь ко всему живому , а не утилитарное <span style="mso-spacerun:yes">&nbsp;</span>отношение <span style="mso-spacerun:yes">&nbsp;</span>или <span style="mso-spacerun:yes">&nbsp;</span>товарно - денежные отношения <span style="mso-spacerun:yes">&nbsp;</span>в <span style="mso-spacerun:yes">&nbsp;</span>их <span style="mso-spacerun:yes">&nbsp;</span>худшем проявлении . Человечество не может искуственно <span style="mso-spacerun:yes">&nbsp;</span>разделяться <span style="mso-spacerun:yes">&nbsp;</span>по <span style="mso-spacerun:yes">&nbsp;</span>рассовым <span style="mso-spacerun:yes">&nbsp;</span>или <span style="mso-spacerun:yes">&nbsp;</span>конфессиональным признакам , это же относится и к единому кинологическому пространству . Условное деление на клубы <span style="mso-spacerun:yes">&nbsp;</span>и <span style="mso-spacerun:yes">&nbsp;</span>клубные системы не должно оказывать негативного воздействия на достижение целей и выполнение задач , которые как воздух , которым мы дышим , являются для всех нас общими .</p>
<p class="MsoNormal">
	<span style="mso-spacerun:yes">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span>Участники нашей конференции обращаются к кинологической общественности мира , к которой может отнести себя каждый , кто просто любит собак , занимается кинологией профессионально или состоит в руководстве какой - либо кинологической организации .</p>
<p class="MsoNormal">
	<span style="mso-spacerun:yes">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span>Мы собрались в Киеве не только и не столько для того , чтобы , в очередной раз , заявить о факте своего существования или подвести итоги проделанной нами совместной работы .Нашими основными задачами является дальнейшее укрепление нашего союза , установление партнёрских взаимоотношений с другими кинологическими организациями , а также всестороннее развитие кинологии во всех её проявлениях .</p>
<p class="MsoNormal">
	<span style="mso-spacerun:yes">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span>Исходя из всего вышесказанного , мы предлагаем :</p>
<p class="MsoNormal">
	1. Преследуя истинно кинологические цели , прекратить политику стагнации и очернения одних клубов по отношению к другим . Кинологи должны заниматься кинологией , а не интригами с попыткой фальсификации информации , с применением нечестных приёмов конкурентной борьбы .</p>
<p class="MsoNormal">
	2. По отношению ко многим породам собак , являющимся национальным достоянием целых стран и народов , следует восстановить историческую справедливость , официально признать и зарегистрировать данные породы , а также закрепить за ними страны их происхождения . Основанием для дальнейшего разведения и развития данных пород считать стандарт , принятый страной происхождения .</p>
<p class="MsoNormal">
	3. Создать консультативный совет , в который входили бы представители наиболее значимых кинологических сообществ , с целью координации дальнейших действий по популяризации и развитию современной кинологии .</p>
<p class="MsoNormal">
	4. Мы выступаем с инициативой проведения совместных кинологических мероприятий ( выставок , соревнований по дрессуре и кинологическим видам спорта ) под лозунгом &quot; Кинология без границ !&quot; , в которых могли бы принимать участие представители любых стран и кинологических сообществ .</p>
<p class="MsoNormal">
	5. Во многих государствах мира политическими и общественными организациями проводится достаточно агрессивная политика по формированию негативного общественного мнения в отношении представителей ряда пород с причислением их к категории<span style="mso-spacerun:yes">&nbsp;&nbsp; </span>&quot; социально - опасных &quot; или<span style="mso-spacerun:yes">&nbsp; </span>&quot; потенциально - агрессивных &quot; . Необходимо приложить максимум усилий , в том числе , с привлечением к решению данной проблемы широких общественных масс , при непосредственном<span style="mso-spacerun:yes">&nbsp; </span>участии масс - медиа , с целью недопущения дальнейшей дискредитации , а , зачастую , и физического уничтожения собак данных пород , как и их вырождения в целом .</p>
<p class="MsoNormal">
	6. Необходимо ужесточить контроль в чистопородном разведении , особенно в части , касающейся использования инбридинга , а также по отношению к генетически передающимся заболеваниям .</p>
<p class="MsoNormal">
	7. Максимум усилий следует направить на приведение &quot; Законов о содержании домашних животных &quot; различных стран к цивилизованным нормам , декларируемым мировым сообществом и<span style="mso-spacerun:yes">&nbsp; </span>&quot; Европейской конвенцией по защите животных &quot; . Используя демократические методы , добиваться ужесточения законодательства <span style="mso-spacerun:yes">&nbsp;</span>ряда <span style="mso-spacerun:yes">&nbsp;</span>стран применительно статьи<span style="mso-spacerun:yes">&nbsp; </span>&quot; О жестоком либо бесчеловечном обращении<span style="mso-spacerun:yes">&nbsp; </span><span style="mso-spacerun:yes">&nbsp;</span>с <span style="mso-spacerun:yes">&nbsp;</span>животными &quot; , исходя из того , что фиктивность <span style="mso-spacerun:yes">&nbsp;</span>наказания порождает <span style="mso-spacerun:yes">&nbsp;</span>безнаказанность .</p>
<p class="MsoNormal">
	8. С целью воспитания будущих поколений в духе гуманизма и социальной ответственности , проводить <span style="mso-spacerun:yes">&nbsp;</span>работу <span style="mso-spacerun:yes">&nbsp;</span>по <span style="mso-spacerun:yes">&nbsp;</span>популяризации <span style="mso-spacerun:yes">&nbsp;</span>собаководства <span style="mso-spacerun:yes">&nbsp;</span>в <span style="mso-spacerun:yes">&nbsp;</span>образовательных <span style="mso-spacerun:yes">&nbsp;</span>учреждениях проведением <span style="mso-spacerun:yes">&nbsp;</span>показательных <span style="mso-spacerun:yes">&nbsp;</span>и <span style="mso-spacerun:yes">&nbsp;</span>методических ( обучающих ) мероприятий . <span style="mso-spacerun:yes">&nbsp;</span>Сделать курсы юных хендлеров и кружки юных любителей собаководства максимально доступными для самых широких слоёв населения , невзирая <span style="mso-spacerun:yes">&nbsp;</span>на <span style="mso-spacerun:yes">&nbsp;</span>экономический либо социальный статус .</p>
<p class="MsoNormal">
	<span style="mso-spacerun:yes">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span>Мы готовы протянуть руку дружбы каждому , кому небезразлична кинология , её настоящее , а , главное , - наше общее будущее . Ибо , как написан Антуан <span style="mso-spacerun:yes">&nbsp;</span>Экзюпери , - &quot; МЫ В ОТВЕТЕ ЗА ТЕХ , КОГО ПРИРУЧИЛИ ! &quot;.</p>
