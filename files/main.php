<p>
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Международный Кинологический Союз (International Kennel Union)&nbsp;&nbsp;&nbsp; зарегистрирован&nbsp; и&nbsp; перерегистрирован&nbsp; в Министерстве Юстиции 18.11.2009 года, номер свидетельства 1798934,&nbsp; как мировое кинологическое сообщество и является общественной организацией.</p>
<p>
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Учредителями Международного Кинологического Союза являются страны: Болгария, Россия, Украина.</p>
<p>
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; МКС-IKU объединяет ведущие национальные кинологические организации мира.</p>
<p>
	&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;&nbsp; В своей деятельности Международный Кинологический Союз опирается&nbsp; на принципы&nbsp; демократичности, открытости, профессионализма. Устав организации учитывает равноправие всех стран членов МКС.&nbsp; Все решения принимаются - коллегиально.&nbsp; Деятельность организации распространяется на все континенты и страны.</p>
<p>
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; МКС имеет свой зарегистрированный логотип (эмблему) с эксклюзивным дизайном. Этот логотип может быть использован только членами организации.</p>
